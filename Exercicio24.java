package Lista_2_Estrutura_De_Decisao;

public class Exercicio24 {
	protected Double horaEntrada;
	protected Double minutoEntrada;
	protected Double horaSaida;
	protected Double minutoSaida;
	
	public Exercicio24() {
		
	}
	
	public Exercicio24(Double horaEntrada, Double minutoEntrada, Double horaSaida, Double minutoSaida) {
		setHoraEntrada(horaEntrada);
		setHoraSaida(horaSaida);
		setMinutoEntrada(minutoEntrada);
		setMinutoSaida(minutoSaida);
	}
	
	public Double getHoraEntrada() {
		return horaEntrada;
	}
	
	public void setHoraEntrada(Double horaEntrada) {
		this.horaEntrada = horaEntrada;
	}
	
	public Double getMinutoEntrada() {
		return minutoEntrada;
	}
	
	public void setMinutoEntrada(Double minutoEntrada) {
		this.minutoEntrada = minutoEntrada;
	}
	
	public Double getHoraSaida() {
		return horaSaida;
	}
	
	public void setHoraSaida(Double horaSaida) {
		this.horaSaida = horaSaida;
	}
	
	public Double getMinutoSaida() {
		return minutoSaida;
	}
	
	public void setMinutoSaida(Double minutoSaida) {
		this.minutoSaida = minutoSaida;
	}
	
	
	
}
