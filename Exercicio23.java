package Lista_2_Estrutura_De_Decisao;

public class Exercicio23 {
	private Double distanciaSinal;
	private String statusSinal;
	public Exercicio23() {
	
	}
	public Exercicio23(Double distanciaSinal, String statusSinal) {
		setDistanciaSinal(distanciaSinal);
		setStatusSinal(statusSinal);
	}
	public Double getDistanciaSinal() {
		return distanciaSinal;
	}
	public void setDistanciaSinal(Double distanciSinal) {
		this.distanciaSinal = distanciSinal;
	}
	public String getStatusSinal() {
		return statusSinal;
	}
	public void setStatusSinal(String statusSinal) {
		this.statusSinal = statusSinal;
	}
	
	
}
